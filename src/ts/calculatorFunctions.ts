export const sum = (number1: number, number2: number): number => {
  return number1 + number2;
};

export const substraction = (number1: number, number2: number): number => {
  return number1 - number2;
};

export const multiplication = (number1: number, number2: number): number => {
  return number1 * number2;
};

export const division = (number1: number, number2: number): number => {
  return number1 / number2;
};
